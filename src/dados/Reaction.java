package dados;

public class Reaction {
	//Atributos
	private int nReactions = 0;
	private String location;
	//Getters and Setters
	public int getnReactions() {
		return nReactions;
	}
	public void setnReactions(int nReactions) {
		this.nReactions = nReactions;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
}
